                                    <h2 class="post-title">
                                        <a class="fontsize" href="/how-to-fight-federal-drug-trafficking-charges">How To Fight Federal Drug Trafficking Charges?</a>
                                    </h2>
                                    <div class="grid-meta">
                                        By
                                        <span class="sep">|</span>
                                        <a class="same" href="">0 comments</a>
