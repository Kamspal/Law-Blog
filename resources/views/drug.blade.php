                                    <div class="post-content1">
                                        <p class="font">
                                            <span>If you have been accused of</span>
                                                <a class="same" href="">
                                                    <i>
                                                    <span>federal drug trafficking charges</span>
                                                    </i>
                                                </a>
                                            <span>, you know that you are going to be facing an uphill battle to get those charges eliminated or even reduced. The federal government takes drug charges of all kinds extremely seriously, but they are even more serious about </span>
                                            <a href="" class="same">
                                                <span>drug trafficking</span>
                                            </a>
                                            <span> charges in Oklahoma. Typically, states will work with federal agencies such as the Drug Enforcement Agency (DEA), the Federal Bureau of Investigation (FBI), and the Bureau of Alcohol, Tobacco, Firearms, and Explosives (ATF) to prosecute you. Everything is dictated by the 
                                            <a href="" class="same">Drug Abuse Prevention and Control Act of 1970.</a>
                                            </span>
                                        </p>
                                        <p class="font">
                                            <span>
                                            When you are dealing with federal drug charges, you are experiencing a completely different world than you would with simple possession. Federal investigators and prosecutors are extreme and have the time, money, and ability to investigate the evidence against you – and they will use it all. You are not going to get off because someone doesn’t show up to court or evidence was lost. These people are organized, experienced, and they want to give you the worst possible punishments. 
                                            </span>
                                        </p>
                                        <p class="font">
                                            <span>
                                            No matter what, they are not on your side. You will want to hire a federal criminal defense lawyer who has experience in drug cases, going to court, settling out of court, and actually showing up for their clients.
                                            </span>
                                        </p>
                                        <p class="font">
                                            <span>
                                            Some lawyers will suggest only settling out of court because that way you “get a better deal.” This isn’t necessarily the case, especially if there are other, more high profile cases that take precedence over yours. It is critical that you call a lawyer that you can trust – which means they get to know you and your case.
                                            </span>
                                        </p>
                                        <h2 class="size1">
                                            <span>Drug Trafficking – Facing Allegations</span>
                                        </h2>
                                        <p class="font">
                                            <a href="" class="same">
                                                <span>Drug trafficking is one of the most serious crimes</span>
                                            </a>
                                            <span> that you can be charged with, and it requires you to be organized and ready to fight. While federal drug trafficking is often talked about as simply the sale or distribution of drugs, it can also include the following: cultivation, possession with intent to distribute, conspiracy to distribute, financing manufacturing, financing sales, transportation, and of course, the sale of drugs.</span>
                                        </p>
                                        <p class="font">
                                            <span>
                                            Sometimes, the charges will hinder on the amount of drugs involved as well as the type of drug. Everything really depends on your case, which is why finding advice online may be so difficult. 
                                            </span>
                                        </p>
                                        <h2 class="size1">
                                            <span>Federal Drug Trafficking Charges Penalties</span>
                                        </h2>
                                        <p class="font">
                                            <span>
                                            With any drug offense, there is a span of penalties that someone may face, depending on the type of drug that was found and the quantity of that drug. Penalties can range from five to 40 years in prison, and, in the most extreme cases, up to $30 million in fines. Often, the highest penalties are reserved for extremely large amounts of PCP, methamphetamines, heroin, fentanyl, cocaine, and prescription drugs.
                                            </span>
                                        </p>
                                        <p class="font">
                                            <span>
                                            If you have been charged with drug crimes before now, you are more likely to face punishments and penalties at the higher end of the spectrum.
                                            </span>
                                        </p>
                                        <h2 class="size1">
                                            <span>How To Fight Federal Drug Trafficking Charges</span>
                                        </h2>
                                        <p class="font">
                                            <span>
                                            Federal drug trafficking charges are hard to fight, but they are not impossible. With the right lawyer, you may be able to prove that you weren’t aware the drugs were in your possession, you weren’t going to sell them, you were transporting them under duress, or any other reason why you should not be charged with drug trafficking.
                                            </span>
                                        </p>
                                        <p class="font">
                                            <span>
                                            You may also want to talk to a lawyer who has experience in long-term federal investigations relating to drug trafficking, including international drug rings. It is critical for your future to have the right lawyer, or you may never be able to live a normal life again.
                                            </span>
                                        </p>
                                        <p class="font">
                                            <span>
                                            Remember that even if you are guilty, you do not deserve to be made an example out of – you are a human being and there are reasons for making the choices that you made. You deserve to give yourself, your loved ones, your family, your friends, and anyone else in your life the chance to live life fully and openly – do not sit back and take what is coming your way.
                                            </span>
                                        </p>
