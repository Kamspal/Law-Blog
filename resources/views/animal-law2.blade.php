                                    <h2 class="post-title">
                                        <a class="fontsize" href="/top-8-things-avoid-real-estate-agent-choose-list-home">ACT Ban Factory Farming But How will it be Enforced</a>
                                    </h2>
                                    <div class="grid-meta">
                                        By
                                        <span class="sep">|</span>
                                        <a class="same" href="">0 comments</a>
                                    </div>
                                        <div class="post-tag"></div>
                                    </div>
                                    <div class="post-content1">
                                        <p class="font">
                                            <span>
                                            This week the Australian Capital Territory (ACT) passed legislation to prohibit the use of battery cages and sow stalls
                                            </span>
                                            <span>
                                            [1]. This is a step forward for animal protection and been hailed as a victory by animal advocacy groups such as Voiceless and Animals Australia.
                                            </span>                                      
                                        </p>
                                        <p class="font">
                                            <span>
                                            Under s9A of the&nbsp;
                                            <i>Animal Welfare (Factory Farming) Amendment Bill 2013</i>
                                                &nbsp;it is an offence for a person to keep a laying fowl in battery cage. A battery cage is defined as a cage that does not allow the fowl to fully stretch, perch, access litter, and lay eggs in a nest. Furthermore, under s9C it is an offence for a person to remove or trim the beak of a fowl for a non-therapeutic purpose.
                                            </span>
                                        </p>
                                        <p class="font">
                                            <span>
                                                <img style="width:300px; height:284px" class="alignleft" src="/img/1.png" alt="">
                                                Practices such as the use of battery cages have been described as “legalised animal cruelty”[2]. My concern is how the new laws banning battery cages going to be enforced when the
                                                <i>&nbsp;Animal Welfare Act 1992</i>
                                                &nbsp;(<i>AWA</i>) in its current state is poorly enforced? I understand that there should be a distinction between legal animal production procedures and animal cruelty, but the issue of enforcement is relevant to both.
                                            </span>
                                        </p>
                                        <p class="font">
                                            <span>
                                            In the ACT, the&nbsp;<i>AWA&nbsp;</i>
                                                empowers inspectors to promote and monitor acceptable standards of care, and protect animals from cruelty or welfare offences[3]. Inspector may enter any property, inspect a premises, and seize any animal if the inspector believes it necessary to do so[4]. In terms of penalties, the maximum penalty for an act of cruelty under the 
                                                <i>AWA</i>
                                                is 100 penalty units ($14,000[5]), imprisonment for 1 month or both[6]. In the ACT, the Territory and Municipal Services Directorate (TAMS) is the lead agency for animal welfare and administers 
                                                the&nbsp;<i>AWA</i>&nbsp;The Executive Director of the Parks and City Services Division in TAMS is appointed as the ACT’s Animal Welfare Authority[7] and consists of three inspectors from RSPCA ACT, six officers from TAMS and all sworn Australian Federal Police officers were authorised as inspectors under the Act[8].
                                            </span>
                                        </p> 
                                        <p class="font">&nbsp;</p>
                                        <h2 class="no-bottom">
                                            <span>
                                                <b>Animal Welfare Prosecution Statistics</b>
                                            </span>
                                        </h2>
                                        <p class="font">
                                            <span>
                                            There is little doubt that RSPCA inspectors are hard-working, dedicated people in a very difficult situation. However what I find concerning is that in 2011-2012, the RSPCA National Statistics reported that RSPCA ACT investigated 1437 cruelty offences with only 2 successful prosecutions[9] (0.05 prosecutions per 10,000).Furthermore, there were no routine inspections in the ACT when in other states inspections were routinely done on feedlots, intensive farms, and abattoirs. For a comparison, RSPCA NSW did not fair much better investigated 14445 cruelty offences with 64 prosecutions (0.08 prosecutions per 10,000).
                                            </span>
                                        </p>
                                        <p class="font">&nbsp;</p>
                                        <h2 class="no-bottom">
                                            <span>
                                                <b>EU ban on battery cages</b>
                                            </span>
                                        </h2>
                                        <p class="font">
                                            <span>
                                            The new legislative changes in ACT are in step with the prohibition of battery cages in the European Union (EU) under the                                            &nbsp;<i>Council Directive 1999/74/EC</i>&nbsp;. Switzerland was one of the earliest countries to incorporate the&nbsp;<i>Directive</i>&nbsp;
                                            into national law by amending the 
                                            &nbsp;<i>Swiss Animal Protection Ordinance 1981</i>&nbsp;
                                            [10]. Switzerland’s prosecution of animal welfare offences varies from canton to canton with the national average of 1.85 per 10,000 persons. In 2012, Valais prosecuted 9 offences (0.28 per 10,000), Fribourg prosecuted 26 offences (0.89 per 10,000), and Neuchâtel prosecuted 28 offences (1.6 per 10,000). The Swiss champion of animal welfare is the canton of St Gallen with 248 prosecutions (5.09 per 10,000)[11].
                                            </span>
                                        </p>
                                        <p class="font">&nbsp;</p>
                                        <p class="font">
                                            <span>
                                            <b>Australia should be prosecuting more offenders</b>
                                            </span>
                                        </p>
                                        <p class="font">
                                            <span>
                                            So why the discrepancy between the rates between Australia and Switzerland? Firstly, 
                                            animal welfare has been enshrined in the&nbsp;<i>Swiss Constitution</i>&nbsp; by stating that in 
                                            relation to non-human gene technology the “dignity of living beings” be taken into account[12]. Secondly, 
                                            the&nbsp;<i>Animal Protection Act</i>[13]&nbsp;&nbsp;and the&nbsp;<i>Animal Protection Ordinance</i>
                                            [14]are federal animal welfare regimes which are enforced by each canton. The reality is that Australia is not ready 
                                            to recognised animals in its&nbsp;<i>Constitution</i>
                                            &nbsp; at this point. However, In order for Australia to improve animal welfare there must be a uniform national approach as in Switzerland. It is also important that this Commonwealth legislation removes terminology such as 
                                            unnecessary[15]&nbsp; or unjustified[16]&nbsp; pain.
                                            </span>
                                        </p>
                                        <p class="font">
                                            <span>
                                                <img class="alignright" src="/img/1a.png" style="width:300px; height:300px" alt="">
                                                The RSPCA of each state investigate over 1000 cruelty offences each year and they do so when it is reasonably necessary. The fact that prosecutions are low must be attributed to argument that an offender can raise as to what is unnecessary pain. The new ACT laws ban specific acts and do not allow for this “wiggle room”.  Therefore, when the new laws come into full effect in 2017 it will be interesting to see how they affect prosecution rates. There will undoubtedly be some producers who do not comply with the new laws, but will they be investigated and prosecuted?
                                            </span>
                                        </p>
                                        <p class="font">
                                            <span>
                                            If prosecution rates remain at the same current low levels legislators will have to address whether more personnel or funding is needed for investigating or prosecuting, or even changes as to what is required to prove an offence.  If there is no change in prosecution rates then the new laws should be considered ineffective because although the legislation bans horrific practices it has not provided any mechanisms to monitor its implementation or given inspectors greater powers to enforce and prosecute when it is breached.
                                            </span>
                                        </p>            
