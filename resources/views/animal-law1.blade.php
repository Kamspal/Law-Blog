                                    <div class="post-content1">
                                        <p class="font">
                                            <span>
                                            Jacob Vanderschoot became the first person to be convicted of serious animal cruelty at the Local Court, in Mt. Druitt, NSW. Vanderschoot used a boning knife to cut the dog Bouncer’s throat and left him lying in his front yard for hours[1]. For this act, Vanderschoot received no jail time and was instead sentenced to a 12 month intensive corrective order.&nbsp; This seems like a good time to talk about the serious cruelty provision under the&nbsp;
                                            <i>Crimes Act.</i> 
                                            </span>
                                        </p>
                                        <p class="font">&nbsp;</p>
                                        <h2 class="no-bottom">
                                            <span>
                                            <b>Serious cruelty provisions</b>
                                            </span>
                                        </h2>
                                        <p class="font">
                                            <span>
                                                <img class="alignleft" src="/img/sheep.png" alt="">
                                                In NSW, animal cruelty offences are legislated under the&nbsp;
                                                <i>Prevention of Cruelty to Animals Act (1979)</i>&nbsp;(POCTAA). Under s5, a person shall not commit an act of cruelty upon an animal. Cruelty is defined as any act of which the animal is unreasonably, unnecessarily or unjustifiably beaten, kicked, killed etc[2]. However, in 2005, the NSW government brought in the
                                                &nbsp;<i>Crimes Amendment (Animal Cruelty)</i>
                                                <i>&nbsp;Bill 2005&nbsp;</i>
                                                which added animal cruelty laws into the existing
                                                &nbsp;<i>Crimes Act (1900)(NSW)</i>
                                                . The amendment was introduced to deal with the worst examples of animal cruelty, that is, cases where offences are committed with the intention of inflicting pain on the animal in circumstances that amount to serious instances of animal cruelty, such as torture, and where the animal is killed, seriously injured or experiences prolonged suffering.  The amendment was also introduced in response to several incidences of offenders intentionally inflicting injury on dogs and horses used by the NSW Police.

                                            </span>
                                        </p>
                                        <p class="font">
                                            <span>
                                                Under
                                                &nbsp;<i>POCTAA</i>
                                                , the RSPCA and the Animal Welfare League (AWL) prosecute the matters with no involvement of the police, however, a new amendment to the 
                                                &nbsp;<i>Law Enforcement (Police Powers and Responsibilities) Act 2002 </i>
                                                has meant that persons suspected of serious cruelty offences can be prosecuted by the police and welfare groups together. This guarantees that a guilty person’s fingerprints would be taken and a subsequent notation made on their criminal record which did not necessarily occur under 
                                                <i>POCTAA</i>[3].
                                            </span>
                                        </p> 
                                        <p class="font">&nbsp;</p>
                                        <h2 class="no-bottom">
                                            <span>
                                                <b>The elements of the offence</b>
                                            </span>
                                        </h2>
                                        <p class="font">
                                            <span>
                                            As previously mentioned, Vanderschoot is the first person convicted of the serious cruelty provision under the&nbsp;
                                            <i>Crimes Act</i>
                                            . He is not, however, the first person to be charged with the offence. In&nbsp;
                                            <i>Larobina v R</i>
                                            [4], the accused was charged after he and the co-accused injected a ferret with an unknown substance. The accused was convicted at the Magistrates Court but was overturned at the District Court after the prosecution failed to make the charge under the correct provision. Being an untested provision, the appeal in&nbsp;
                                            <i>Larobina v R &nbsp;</i>
                                            concerning the elements of the offence. Therefore, if prosecutors are struggling with the concept it certainly warrants a detailed investigation.
                                            </span>
                                        </p>
                                        <p class="font">
                                            <span>
                                            Under s530, in order to be charged with serious animal cruelty a person must express the intention to inflict severe pain by torturing, beating or committing any other serious act of cruelty on an animal, and also, kill or serious injury, or cause prolonged suffering to the animal. As with any piece of legislation, statutory interpretation is needed to determine the components that are necessary to be made out by the prosecution.&nbsp;  There are three elements to s530(1) of the
                                            <i>Crimes Act</i>
                                            . There must be:
                                            </span>
                                        </p>
                                        <ol class="font">
                                            <li class="marginb">
                                                <span>an intention to inflict serious pain</span>
                                            </li>
                                            <li class="marginb">
                                                <span>an act that was committed upon the animal</span>
                                            </li>
                                            <li class="marginb">
                                                <span>xwhere the animal is killed, seriously injured, or had suffered for a prolonged period</span>
                                            </li>
                                        </ol>  
                                        <p class="font">
                                            <span>For the offence to be made out, these 3 elements must be proven beyond a reasonable doubt by the prosecution[5]. The maximum penalty for this offence is imprisonment for 5 years.</span>
                                        </p>
                                        <p class="font">&nbsp;</p>
                                        <h2 class="no-bottom">
                                            <span>
                                                <b>No jail time</b>
                                            </span>
                                        </h2> 
                                        <p class="font">
                                            <span>
                                            Magistrate Corry said the defendant had “caused severe injuries” to Bouncer and “allowed it (sic) to suffer unnecessarily for hours with its throat cut[6]”. Vanderschoot was found guilty so presumably satisfied the 3 elements of the offence. Despite this, Vanderschoot received a 12 month intensive corrections order to be served in the community, subject to strict conditions. In sentencing, the magistrate considered the defendant’s age and a psychological report presented to the court[7]. Maximum penalties are rarely even given during sentencing so it was unlikely that Vanderschoot would have received 5 years. In sentencing, a magistrate can take into account, the nature and seriousness of the crime, the circumstances of the offender, the need to deter the convicted offender from committing further crimes, and the need to deter others from committing similar crimes.  This case was heard at the Magistrates Court and therefore is not reported as in the higher courts. However, it is obvious from the sentence that the magistrate put more weight on the circumstances of Vanderschoot rather than the seriousness of the crime[8].
                                            </span>
                                        </p>
                                        <p class="font">
                                            <span>
                                                <img class="alignright" src="/img/4.png" alt="">
                                                While recognising the importance of rehabilitation, it is hard to reconcile that Vanderschoot was found guilty of serious animal cruelty and not is not serving his sentence in jail. A person who commits this type of offence should be separated from the community to protect the public, as with any other violent crime. A 2002 study was the first to demonstrate the significant association between animal cruelty,anti-social personality traits and polysubstance abuse[9]. There is also evidence that animal cruelty and lead to bullying[10], child abuse, and other violent crimes including murder[11].  The seriousness of animal cruelty is undeniable and needs to be addressed by the judicial system.
                                            </span>
                                        </p>
                                        <p class="font">
                                            <span>
                                            Finally, it is worth mentioning again that as this case was heard at the Magistrates Court, and has no precedent value. This means that the appellate courts will take this judgment into consideration but is not binding. Therefore, this decision does not necessarily mean that every person guilty of serious animal cruelty will receive this sentence.  Its is within the judge’s discretion to give a greater or even lesser sentence. This being said, Bouncer, who has now recovered, deserves better.  As does the AWL Vets and Vet Nurses who put him back together, the inspectors who investigated the offence, and the AWL, and DPP lawyers who prosecuted the matter.
                                            </span>
                                        </p>            
