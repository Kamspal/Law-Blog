                                    <h2 class="post-title">
                                        <a class="fontsize" href="/top-8-things-avoid-real-estate-agent-choose-list-home">Top 8 Things to Avoid in the Real Estate Agent you Choose to List your Home</a>
                                    </h2>
                                    <div class="grid-meta">
                                        By
                                        <span class="sep">|</span>
                                        <a class="same" href="">0 comments</a>
                                    </div>
                                        <div class="post-tag"></div>
                                    </div>
                                    <div class="post-content1">
                                        <p class="font">
                                            <span>
                                            <strong>1</strong>&nbsp;*Agents that are rude. Sellers often pick these agents under the mistaken impression that it will benefit them because they have a “tough agent”. But in most real estate markets, other agents typically don’t want to deal with them and are less likely to show their listings. Additionally, when buyers run into a rude listing agent, they are more inclined to come away with a negative impression of the property.
                                            </span>
                                        </p>
                                        <p class="font">
                                            <span>
                                            <strong><img src="/img/2.png" alt="" class="alignleft" style="width: 254px; height:300px">2</strong>&nbsp;*Agents that make it more difficult to show their listings. In most states agents, can put a lockbox on a house. Some agents in these states will put in the listing that you have to make an appointment with the listing agent. Since the buyer’s agent has to call and make appointment instead of just showing the listing, this dramatically cuts down on the number of showings these sellers receive.
                                            </span>
                                        </p>
                                        <p class="font">&nbsp;</p>
                                        <p class="font">
                                            <span>
                                                <strong>3</strong>&nbsp;*Agents that only work as real estate agents occasionally. While it seems like a good idea to use your brother’s friend that has sold 3 houses in the last 5 years, it’s probably not. These agents don’t know the market as well as a full time agent. Additionally, they don’t understand what is typical in a real estate transaction in your region and an experienced buyer’s agent can use this to their client’s advantage at your expense. If you were going in for a root canal you would not want to use a dentist that has only seen 3 patients in the last 5 years. In the same way, you don’t want to hand over the sale of your largest asset to someone that is inexperienced.
                                            </span>
                                        </p>
                                        <p class="font">&nbsp;</p>
                                        <p class="font">
                                            <span>
                                                <strong>4</strong>&nbsp;*Agents that are so desperate they try and steal clients away from every agent that shows the house. While this is rarely successful, it does make an agent think twice before showing the listing to another client knowing there is a hungry vulture waiting there for them.
                                            </span>
                                        </p>
                                        <p class="font">&nbsp;</p>
                                        <p class="font">
                                            <span>
                                                <strong>5</strong>&nbsp;*Agents that don’t work on the weekends. Real Estate is a 7 days a week business. If your agent is not answering the phone on the weekends, you can expect that the number of showings you will receive is going to be cut down. Sometimes buyers are in from out of state for the weekend and are planning on making an offer while they are in town. They usually narrow it down to two or three properties. While your agent does not return calls about your property over the weekend, the listing agent for the other property probably will answer the buyer’s questions about their client’s property. When it comes time to make an offer, the buyer is going to make an offer on the house they know more about and feel more comfortable with.
                                            </span>
                                        </p>
                                        <p class="font">&nbsp;</p>
                                        <p class="font">
                                            <span>
                                                <strong>6</strong>&nbsp;*Agents that have a bizarre set of rules that is atypical for the industry. This can come in many forms. Some agents want all correspondence with them to be done by fax instead of telephone or email. If the option fee is usually $100 and your agent is demanding $500 you can expect that their eccentric rules is turning away potential buyers. Being eccentric is great with art, but in a real estate transaction, it usually makes people wary.
                                            </span>
                                        </p>
                                        <p class="font">&nbsp;</p>
                                        <p class="font">
                                            <span>
                                                <strong>7</strong>&nbsp;*Agents that don’t take photos of your property. There is no good excuse for not taking photos of your property. If your agent is only going to take 1 or worse 0 photos, it’s time to find a new agent.
                                            </span>
                                        </p>
                                        <p class="font">&nbsp;</p>
                                        <p class="font">
                                            <span>
                                            <strong><img src="/img/3.png" alt="" class="alignright1" style="width: 300px; height:168px">8</strong>&nbsp;*Agents that don’t have a website. As real estate has become more of a business based on technology, with buyers looking online for houses, and more of the real estate transaction taking place through email, you want your agent to be operating in the 21st century. If your agent doesn’t have a website it’s a good sign that they are still practicing real estate in the age of dinosaurs.
                                            </span>
                                        </p>
